int GeometryDimension(int geom)
{
	int d = -1;

	switch (geom)
	{
	case BasicGeomType::Point:
	case BasicGeomType::Line:
		d = 1; break;
	case BasicGeomType::Triangle:
	case BasicGeomType::Quadrilateral:
		d = 2; break;
	default:
		break;
	}

	return d;
}


